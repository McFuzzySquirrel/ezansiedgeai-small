#!/usr/bin/env python3
"""Cross-platform embedding parity validation for P0-006.

Compares embeddings generated by the Python path with those generated
by the Android (MediaPipe) path to ensure they produce compatible vectors.

The workflow:
1. Run `benchmark_embedding.py --parity-check` to generate Python embeddings
2. Run the Android spike benchmark to generate Android embeddings
3. Export Android embeddings to `results/parity-android.json`
4. Run this script to compare them

Usage:
    python scripts/validate_parity.py
    python scripts/validate_parity.py --threshold 0.99
"""

import argparse
import json
import sys
from pathlib import Path

import numpy as np


def cosine_similarity(a: np.ndarray, b: np.ndarray) -> float:
    norm_a = np.linalg.norm(a)
    norm_b = np.linalg.norm(b)
    if norm_a == 0 or norm_b == 0:
        return 0.0
    return float(np.dot(a, b) / (norm_a * norm_b))


def load_parity_file(path: Path) -> dict:
    if not path.exists():
        print(f"ERROR: File not found: {path}")
        print("Generate it first:")
        if "python" in str(path):
            print("  python scripts/benchmark_embedding.py --parity-check")
        else:
            print("  Run the Android spike benchmark and export embeddings")
        sys.exit(1)

    with open(path) as f:
        return json.load(f)


def main():
    parser = argparse.ArgumentParser(description="Validate cross-platform embedding parity")
    parser.add_argument("--threshold", type=float, default=0.99,
                        help="Minimum cosine similarity for parity (default: 0.99)")
    parser.add_argument("--python-file", default="results/parity-python.json")
    parser.add_argument("--android-file", default="results/parity-android.json")
    args = parser.parse_args()

    results_dir = Path(__file__).parent.parent / "results"
    python_data = load_parity_file(results_dir / args.python_file.split("/")[-1])
    android_data = load_parity_file(results_dir / args.android_file.split("/")[-1])

    print("=" * 60)
    print("Cross-Platform Embedding Parity Validation — P0-006")
    print("=" * 60)
    print(f"Python platform:  {python_data.get('platform', 'unknown')}")
    print(f"Android platform: {android_data.get('platform', 'unknown')}")
    print(f"Python dim:       {python_data.get('embedding_dim', 'unknown')}")
    print(f"Android dim:      {android_data.get('embedding_dim', 'unknown')}")
    print(f"Threshold:        {args.threshold}")
    print()

    python_embeddings = python_data.get("embeddings", {})
    android_embeddings = android_data.get("embeddings", {})

    common_ids = set(python_embeddings.keys()) & set(android_embeddings.keys())
    if not common_ids:
        print("ERROR: No common document IDs between Python and Android embeddings")
        sys.exit(1)

    print(f"Comparing {len(common_ids)} embeddings...")
    print()

    similarities = []
    all_pass = True

    for doc_id in sorted(common_ids):
        py_vec = np.array(python_embeddings[doc_id], dtype=np.float32)
        android_vec = np.array(android_embeddings[doc_id], dtype=np.float32)

        if len(py_vec) != len(android_vec):
            print(f"  {doc_id}: DIMENSION MISMATCH (py={len(py_vec)}, android={len(android_vec)})")
            all_pass = False
            continue

        sim = cosine_similarity(py_vec, android_vec)
        similarities.append(sim)
        status = "✅" if sim >= args.threshold else "❌"
        if sim < args.threshold:
            all_pass = False
        print(f"  {doc_id}: cosine={sim:.6f} {status}")

    print()
    if similarities:
        avg_sim = np.mean(similarities)
        min_sim = np.min(similarities)
        max_sim = np.max(similarities)
        print(f"Average similarity: {avg_sim:.6f}")
        print(f"Min similarity:     {min_sim:.6f}")
        print(f"Max similarity:     {max_sim:.6f}")

    print()
    if all_pass:
        print("✅ PARITY CHECK PASSED — embeddings are compatible across platforms")
    else:
        print("❌ PARITY CHECK FAILED — embeddings differ significantly")
        print("   This may indicate different tokenization, normalization, or model weights")
        print("   between the Python and Android inference paths.")

    # Save results
    output = {
        "threshold": args.threshold,
        "passed": all_pass,
        "common_ids": len(common_ids),
        "similarities": {doc_id: float(sim) for doc_id, sim in zip(sorted(common_ids), similarities)},
        "avg_similarity": float(np.mean(similarities)) if similarities else 0,
        "min_similarity": float(np.min(similarities)) if similarities else 0,
    }
    output_path = results_dir / "parity-results.json"
    with open(output_path, "w") as f:
        json.dump(output, f, indent=2)
    print(f"\nResults saved to: {output_path}")


if __name__ == "__main__":
    main()
